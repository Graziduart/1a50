public class Main {
    public static void main(String[] args) {
        int i = 1; // Iniciando o contador com 1 (primeiro número ímpar)

        // Utilizando o loop 'while' para exibir os números ímpares de 1 a 50
        while (i <= 50) {
            System.out.println(i);
            i += 2; // Incrementando 2 para pegar o próximo número ímpar
        }
    }
}
